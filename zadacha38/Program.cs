//  Задача 38: Задайте массив вещественных чисел. Найдите разницу между максимальным и минимальным элементов массива.

double[] arr = new double[] { 3.5, 2.1, 4.8, 1.9, 5.2 }; // создаем массив вещественных чисел
double min = arr[0]; // переменная для хранения минимального элемента
double max = arr[0]; // переменная для хранения максимального элемента

for (int i = 1; i < arr.Length; i++)
{
    if (arr[i] < min)
    {
        min = arr[i]; // обновляем значение минимального элемента
    }
    if (arr[i] > max)
    {
        max = arr[i]; // обновляем значение максимального элемента
    }
}

double difference = max - min; // вычисляем разницу между максимальным и минимальным элементами
Console.WriteLine("Разница между максимальным и минимальным элементами: " + difference);


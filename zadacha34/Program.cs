//Задача 34: Задайте массив заполненный случайными положительными трёхзначными числами. Напишите
// программу, которая покажет количество чётных чисел в массиве.

int[] arr = new int[4]; // создаем массив на 4 элемента
int count = 0; // переменная для хранения количества четных чисел
Random rnd = new Random(); // создаем генератор случайных чисел

for (int i = 0; i < arr.Length; i++)
{
    arr[i] = rnd.Next(100, 1000); // заполняем массив случайными трехзначными числами
    if (arr[i] % 2 == 0) // проверяем, является ли число четным
    {
        count++; // увеличиваем счетчик четных чисел
    }
}

Console.WriteLine("Количество четных чисел в массиве: " + count); // выводим результат на экран
